# Curso Python #013 - Estrutura de repetição for

for x in range(0, 6):       # ele vai fazer de 1 até 5, ele não considera o ultimo numero, para no 5 neste caso
    print('{} - Oi'.format(x))
print('FIM')


for y in range(6, 0, -1):       # a terceira casa dentro do range é a interação, ou passo, neste caso ele vai voltar de 1 em 1 até o 0
    print('{} - Oi'.format(y))
print('FIM')

